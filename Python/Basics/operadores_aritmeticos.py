"""  Los operadores son simboloes que se utilizan para realizar distintos 
    tipos de operaciones
"""

#Operadores aritmeticos

num1 = 10
num2 = 37

#suma (+)

sum = num1 + num2
print("Operacion de Suma: ", sum)

#Si yo sumo strings (concatenar)

sum = "Delvis " + "Sanabria"
print("Operacion de Suma de strings: ", sum)

#Resta (-)

rest = num1 - num2
print("Operacion de Resta: ", rest)

#Multiplicacion (*)

mult = num1 * num2
print("Operacion de Multiplicacion: ", mult)

#Multiplicacion de strings
mult = "Delvis " * num2
print("Operacion de Multiplicacion de strings: ", mult)

mult = 10.2 * 5.75
print("Operacion de Multiplicacion con decimales: ", mult)

mult = 10 * 5.75
print("Operacion de Multiplicacion con decimales: ", mult)

#Division (/)

div = num1 / num2
print("Operacion de Division: ", div)

#Modulo (%)

mod = num1 % num2
print("Operacion de Modulo: ", mod)

#Potencia (**)

pot = num1 ** num2
print("Operacion de Potencia: ", pot)

#Division entera (//)

divEnt = num1 // num2
print("Operacion de Division entera: ", divEnt)

