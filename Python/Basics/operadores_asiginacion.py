#Los operadores de asignacion nos sirven para asignar valores a variables
num1 = 10
num2 = 37

#asignacion simple (=)

num1 = 12
num2 = 40

#asignacion compuesta (+=) Suma

num1 += 5
num2 += 5

print("Operacion de Suma: ", num1)
print("Operacion de Suma: ", num2)

#asignacion compuesta (-=) Resta

num1 -= 5
num2 -= 5

print("Operacion de Resta: ", num1)
print("Operacion de Resta: ", num2)

#asignacion compuesta (*=) Multiplicacion

num1 *= 5
num2 *= 5

print("Operacion de Multiplicacion: ", num1)
print("Operacion de Multiplicacion: ", num2)

#asignacion compuesta (/=) Division

num1 /= 5
num2 /= 5

print("Operacion de Division: ", num1)
print("Operacion de Division: ", num2)

#asignacion compuesta (%=) Modulo

num1 %= 5
num2 %= 5

print("Operacion de Modulo: ", num1)
print("Operacion de Modulo: ", num2)