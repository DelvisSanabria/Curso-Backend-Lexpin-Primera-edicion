def sum():
  num1 = int(input("Ingrese el primer numero: "))
  num2 = int(input("Ingrese el segundo numero: "))
  return num1 + num2

def subtract():
  num1 = int(input("Ingrese el primer numero: "))
  num2 = int(input("Ingrese el segundo numero: "))
  return num1 - num2

def square():
  num = int(input("Ingrese un numero: "))
  return num * num  