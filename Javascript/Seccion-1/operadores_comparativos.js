//Los operadores comparativos en JavaScript nos permiten comparar dos valores y nos devuelven un valor booleano (true o false) dependiendo del resultado de la comparaciÃ³n:

let num1 = 1;
let num2 = 5;

// Igualdad : se representa con el operador == y nos devuelve true si los valores son iguales (no compara el tipo de dato)

console.log(num1 == num2); // false

// igualdad estricta : se representa con el operador === y nos devuelve true si los valores y el tipo de dato son iguales
console.log(num1 === num2); // false

// Desigualdad : se representa con el operador != y nos devuelve true si los valores no son iguales (no compara el tipo de dato)

console.log(num1 != num2); // true

// Desigualdad estricta : se representa con el operador !== y nos devuelve true si los valores o el tipo de dato no son iguales
console.log(num1 !== num2); // true

// Mayor que : se representa con el operador > y nos devuelve true si el valor de la variable de la izquierda es mayor que el valor de la variable de la derecha
console.log(num1 > num2); // false

// Menor que : se representa con el operador < y nos devuelve true si el valor de la variable de la izquierda es menor que el valor de la variable de la derecha
console.log(num1 < num2); // true

// Mayor o igual que : se representa con el operador >= y nos devuelve true si el valor de la variable de la izquierda es mayor o igual que el valor de la variable de la derecha
console.log(num1 >= num2); // false

// Menor o igual que : se representa con el operador <= y nos devuelve true si el valor de la variable de la izquierda es menor o igual que el valor de la variable de la derecha
console.log(num1 <= num2); // true